    def encode(self, x, seg):
        h = self.encoder(x)
        h = self.quant_conv(h)
        b, c, hh, ww = h.shape
        h = F.interpolate(h, size = x.shape[-2:], mode = 'bilinear', align_corners = False)
        h = poolfeat(h, seg, avg = True)
        h = h.permute(0, 2, 1)
        sp_num_axis = int(math.sqrt(h.shape[2]))
        quant, emb_loss, info = self.quantize(h.view(b, c, sp_num_axis, sp_num_axis))
        quant = upfeat(quant.view(b, c, -1).permute(0, 2, 1), seg)
        quant = F.interpolate(quant, size = (hh, ww), mode = 'bilinear', align_corners = False)
        return quant, emb_loss, info

    def decode(self, quant):
        quant = self.post_quant_conv(quant)
        dec = self.decoder(quant)
