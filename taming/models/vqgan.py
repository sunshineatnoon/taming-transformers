def poolfeat(input, prob, avg = True):
    """ A function to aggregate superpixel features from pixel features

    Args: 
      input (tensor): input feature tensor.
      prob (tensor): one-hot superpixel segmentation.
      avg (bool, optional): average or sum the pixel features to get superpixel features
    
    Returns:
      cluster_feat (tensor): the superpixel features
    
    Shape:
      input: (B, C, H, W)
      prob: (B, N, H, W)
      cluster_feat: (B, N, C)
    """ 
    B, C, H, W = input.shape
    B, N, H, W = prob.shape
    prob_flat = prob.view(B, N, -1)
    input_flat = input.view(B, C, -1)
    cluster_feat = torch.matmul(prob_flat, input_flat.permute(0, 2, 1))
    if avg:
        cluster_sum = torch.sum(prob_flat, dim = -1).view(B, N , 1)
        cluster_feat = cluster_feat / (cluster_sum + 1e-8)
    return cluster_feat

def upfeat(input, prob):
    """ A function to compute pixel features from superpixel features

    Args: 
      input (tensor): superpixel feature tensor.
      prob (tensor): one-hot superpixel segmentation.
    
    Returns:
      reconstr_feat (tensor): the pixel features.
    
    Shape:
      input: (B, N, C)
      prob: (B, N, H, W)
      reconstr_feat: (B, C, H, W)
    """
    B, N, H, W = prob.shape
    prob_flat = prob.view(B, N, -1)
    reconstr_feat = torch.matmul(prob_flat.permute(0, 2, 1), input)
    reconstr_feat = reconstr_feat.view(B, H, W, -1).permute(0, 3, 1, 2)

    return reconstr_feat

def label2one_hot_torch(labels, C=14):
    """ Converts an integer label torch.autograd.Variable to a one-hot Variable.

    Args:
      labels(tensor) : segmentation label
      C (integer) : number of classes in labels

    Returns:
      target (tensor) : one-hot vector of the input label

    Shape:
      labels: (B, 1, H, W)
      target: (B, N, H, W)
    """
    b,_, h, w = labels.shape
    one_hot = torch.zeros(b, C, h, w, dtype=torch.long).to(labels)
    target = one_hot.scatter_(1, labels.type(torch.long).data, 1) #require long type

    return target.type(torch.float32)

def get_slic(image):
    image = image.cpu()
    B, _, H, W = image.shape
    sp_num = (H//16)*(W//16)
    labels = []
    for i in range(B):
        img_i = image[i].permute(1, 2, 0).numpy()
        img_i = np.array(img_i * 255, dtype = np.uint8)
        slic_i = slic(img_i, n_segments=sp_num, compactness=10, start_label=0, min_size_factor=0.3)
        slic_i = torch.from_numpy(slic_i)
        labels.append(slic_i)
    labels = torch.stack(labels).unsqueeze(1).cuda()
    labels[labels >= sp_num] = sp_num - 1
    seg = label2one_hot_torch(labels, C = sp_num)
    return seg

class VQModelSP(pl.LightningModule):
    def __init__(self,
                 ddconfig,
                 lossconfig,
                 n_embed,
                 embed_dim,
                 ckpt_path=None,
                 ignore_keys=[],
                 image_key="image",
                 colorize_nlabels=None,
                 monitor=None
                 ):
        super().__init__()
        self.image_key = image_key
        self.encoder = Encoder(**ddconfig)
        self.decoder = Decoder(**ddconfig)
